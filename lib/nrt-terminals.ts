/**
 * 航空会社IATAコードから成田のターミナル番号を返す
 * データがない場合は null
 */
export function getNrtTerminal(airlineIata: string): string | null {
  return NRT_TERMINAL_MAP[airlineIata.toUpperCase()] ?? null
}

/**
 * フライトIATA（例: NH832）から航空会社コードを抽出してターミナルを返す
 */
export function getNrtTerminalFromFlight(flightIata: string): string | null {
  const match = flightIata.toUpperCase().match(/^([A-Z]{2,3})\d/)
  if (!match) return null
  return getNrtTerminal(match[1])
}
